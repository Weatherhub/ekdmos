      SUBROUTINE WRTEXT(KFILDO,CFILTXT,NVALUE,DATA,IER)
C
C        HISTORY
C
C        JULY      2014   ENGLE       CREATED.
C
C        PURPOSE
C
C           TO WRITE OUT A GRIDDED TDLPACK RECORD TO A FORMATTED
C           FILE (I.E. ASCII).
C
C        VARIABLES (INPUT/OUTPUT)
C
C              KFILDO = UNIT NUMBER OF DEFAULT PRINT FOR MOS-2000
C                       ROUTINES. HERE KFILDO IS A SCRATCH FILE.
C             CFILTXT = CHARACTER*60 VARIABLE THAT CONTAINS THE NAME OF
C                       THE TEXT OUTPUT FILE.
C              NVALUE = NUMBER OF DATA VALUES TO WRITE TO CFILTXT.
C             DATA(I) = ARRAY HOLDING THE DATA VALUES FOR A RECORD, (I=1,NVALUE)
C                 IER = ERROR RETURN VALUE FROM SUBROUTINES.
C
C        VARIABLES (INTERNAL)
C
C                 IOS = HOLDS THE IOSTAT VALUE FROM FORTRAN I/O STATEMENTS.
C
      IMPLICIT NONE
C        INPUT/OUTPUT VARIABLES
      INTEGER, INTENT(IN) :: KFILDO
      CHARACTER*60, INTENT(IN) :: CFILTXT
      INTEGER, INTENT(IN) :: NVALUE
      REAL, INTENT(IN) :: DATA(NVALUE)
      INTEGER, INTENT(INOUT) :: IER
C        INTERNAL VARIABLES
      INTEGER :: I,IOS
C
C        OPEN FORMATTED FILE WRITING.
C
      OPEN(UNIT=50,FILE=CFILTXT,FORM="FORMATTED",
     1     IOSTAT=IOS,ERR=100,STATUS="REPLACE")
C
C        LOOP THROUGH ALL DATA VALUES AND WRITE TO TEXT
C        FILE.
C
      DO I=1,NVALUE
         WRITE(50,*,IOSTAT=IOS,ERR=110)DATA(I)
      END DO
C
C        STOP PROGRAM IF THERE IS TROUBLE OPENING TEXT
C        OUTPUT FILE
C
 100  IF(IOS.NE.0)THEN
         WRITE(6,105)TRIM(CFILTXT),IOS
 105     FORMAT(/' error: Trouble opening text output file ',
     1           A,'.  iostat = ',I3/)
         CALL ITDLP_STOP
      ENDIF
C
C        STOP PROGRAM IF THERE IS TROUBLE WRITING TO TEXT
C        OUTPUT FILE
C
 110  IF(IOS.NE.0)THEN
         WRITE(6,115)TRIM(CFILTXT),IOS
 115     FORMAT(/' error: Trouble writing to text output file ',
     1           A,'.  iostat = ',I3/)
         CALL ITDLP_STOP
      ENDIF
C
      CLOSE(50)
C
      RETURN
      END SUBROUTINE WRTEXT
