      SUBROUTINE RDSTDIN(KFILIN,IREC)
C
C        HISTORY
C
C        AUGUST    2014   ENGLE       CREATED FOR ITDLP.
C
C        PURPOSE
C
C            TO READ FROM STANDARD INPUT (UNIT=5) ITDLP (SIMPLE/DEFAULT)
C            OUTPUT FROM A PREVIOUS EXECUTION OF THE PROGRAM.  IT IS
C            ASSUMED THAT THE PREVIOUS EXECUTION IS THE SAME FILE.
C
C        VARIABLES (INPUT/OUTPUT)
C
C              KFILIN = UNIT FOR THE INPUT TDLPACK FILE. (INPUT)
C                IREC = THE RECORD NUMBER READ FROM STANDARD INPUT
C                       (OUTPUT)
C
C        VARIABLES (INTERNAL)
C
C               CTEMP = CHARACTER*256 TO HOLD THE STANDARD INPUT
C                       ASCII TEXT.
C                 IDX = POSITION IN CTEMP OF THE FIRST COLON.
C                 IOS = IOSTAT RETURN VALUE FROM IO STATEMENTS.
C               
      IMPLICIT NONE
C        INPUT/OUTPUT VARIABLES
      INTEGER, INTENT(IN) :: KFILIN
      INTEGER, INTENT(OUT) :: IREC
C        INTERNAL VARIABLES
      CHARACTER(LEN=256) :: CTEMP
      INTEGER :: IDX,IOS
C        INITIALIZE VARIABLES
      IOS=0
C
C        READ FROM STANDARD INPUT
C
      READ(5,FMT='(A256)',IOSTAT=IOS,ERR=900,END=999)CTEMP
C
C        DETERMINE THE POSTION OF THE FIRST ":", THEN PERFORM
C        AN INTERNAL READ FROM CTEMP INTO IREC.
C
      IDX=INDEX(CTEMP,':')
      READ(CTEMP(1:IDX-1),FMT='(I<IDX-1>)',IOSTAT=IOS,
     1     ERR=900,END=999)IREC
C
 900  IF(IOS.GT.0)THEN
          WRITE(6,9000)IOS
 9000     FORMAT(/' error: Trouble reading from standard input, ',
     1            'IOS = ',I0.1/)
          CALL ITDLP_STOP(KFILIN)
      ENDIF
C
 999  RETURN
      END SUBROUTINE RDSTDIN
