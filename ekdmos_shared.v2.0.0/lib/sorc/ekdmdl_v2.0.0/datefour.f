      SUBROUTINE DATEFOUR(NDATE,NDATE_FOUR,NNRR)
C
C        AUGUST 1998   WEISS   TDL   MOS-2000
C
C        PURPOSE 
C            GENERATE FOUR DATES FOR DUMY READS OF 
C            MAX MIN INPUT PARAMETERS. THE FOUR DATES WILL
C            CORRESPOND TO THE HOURS 00,06,12, AND 18Z, REFERRED
C            TO AS PRIME HOURS.
C
C        VARIABLES:
C               IDIFF = DIFFERENCE IN THE NUMBER OF HOURS BETWEEN
C                       NDATE AND CLOSEST PRIME HOUR (INTERNAL).
C                 IHR = HOUR COMPONENT OF NDATE (INTERNAL). 
C               JDIFF = DIFFERENCE IN THE NUMBER OF HOURS BETWEEN
C                       NDATE1 AND COMPONENT OF NDATE_FOUR (INTERNAL).
C               NDATE = THE DATE/TIME FOR WHICH PREDICTOR IS NEEDED
C                       (INPUT).
C              NDATE1 = CURRENT PROCESSING DATE (INTERNAL).
C       NDATE_FOUR(N) = THE FOUR GENERATED DATES TO BE USED FOR
C                       DUMMY READS, WHERE N=1,4 (OUTPUT).
C                NNRR = NUMBER OF HOURS TO LOOK BACK (INPUT). 
C               
C
      INTEGER NDATE,NDATE1,NDATE_FOUR(4),IDIFF,IHR,NNRR
C
C        STEP 1. INITIALIZE NDATE_FOUR 
      DO 10 I=1,4
        NDATE_FOUR(I)=0
 10   CONTINUE
      NDATE1=0
C
C        STEP 2. GENERATE DATES FOR NDATE_FOUR
C
      CALL UPDAT(NDATE,-NNRR,NDATE1)
      IHR=MOD(NDATE1,100)
      IDIFF=MOD(IHR,6)
C
      DO 15 I=1,4
        JDIFF=6*(I-1)
        CALL UPDAT(NDATE1,JDIFF,NDATE_FOUR(I))
 15   CONTINUE
C
      RETURN 
      END
