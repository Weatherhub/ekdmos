      SUBROUTINE DATPRS(KFILDO,NDATE,JDATE)
C
C        AUGUST 2000   GLAHN   TDL   LAMP-2000
C
C        PURPOSE
C            TO PARSE A DATE IN NDATE IN FORM YYYYMMDDHH INTO FOUR
C            COMPONENT PARTS IN JDATE(J) (J=1,4), THE PARTS BEING
C            YYYY, MM, DD, AND HH.
C
C        DATA SET USE
C            KFILDO - UNIT NUMBER OF OUTPUT (PRINT) FILE.  (OUTPUT)
C
C        VARIABLES
C              KFILDO = UNIT NUMBER OF OUTPUT (PRINT) FILE.  (INPUT)
C               NDATE = DATE IN FORM YYYYMMDDHH.  (INPUT)
C            JDATE(J) = THE PARSED DATE (J=1,4), THE COMPONENTS BEING
C                       YYYY, MM, DD, AND HH.  (OUTPUT)
C
C        NONSYSTEM SUBROUTINES USED
C            NONE
C
      DIMENSION JDATE(4)
C
      JDATE(1)=NDATE/1000000 
      JDATE(2)=NDATE/10000-JDATE(1)*100
      JDATE(3)=NDATE/100-JDATE(1)*10000-JDATE(2)*100
      JDATE(4)=MOD(NDATE,100)
C
 200  RETURN
      END
