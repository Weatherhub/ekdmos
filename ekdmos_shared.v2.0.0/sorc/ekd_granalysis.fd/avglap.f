               SUM=SUM+MAX(XLAPSE(LOC),0.)
C                  DO NOT SUM NEGATIVE VALUES.
               KOUNT=KOUNT+1
C
               WRITE(KFILDO,2027)K,XLAPSE(LOC),SUM,KOUNT
 2027          FORMAT(' AT 2027 IN AVGLAP--K,XLAPSE(LOC),SUM,KOUNT',
     1                I6,2F10.4,I4)
