 2190       FORMAT('     THIS DIAGNOSTIC WILL NOT PRINT AGAIN FOR',
     1             ' THIS ZERO MODIFICATION.  COUNTED AS ONE ISTOP',
     2             ' ERROR.')
            JFIRST=JFIRST+1
         ENDIF
