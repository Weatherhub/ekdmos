            IF(CCALL(K).EQ.'PAKP    ')THEN
               WRITE(KFILDO,2025)K,LIST(K),CCALL(K),CCALLD(LIST(K)),
     1              XLAPSE(LOC),IALOC(LIST(K),M),
     2              CCALL(IALOC(LIST(K),M)),NAME(IALOC(LIST(K),M)),
